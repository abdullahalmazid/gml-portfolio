// Define the order of colors for bulk input
export const COLOR_KEYS = [
  '--bg-primary', '--bg-secondary', '--bg-tertiary',
  '--text-primary', '--text-secondary', '--text-muted',
  '--accent', '--accent-hover', '--accent-light',
  '--border', '--card-bg'
];

// Helper to parse comma-separated string
export function parseCommaSeparatedColors(str) {
  if (!str) return null;
  const parts = str.split(',').map(s => s.trim());
  if (parts.length !== COLOR_KEYS.length) return null;
  
  const colors = {};
  COLOR_KEYS.forEach((key, i) => {
    // Basic validation to ensure it looks like a hex code
    if (/^#[0-9A-Fa-f]{6}$/.test(parts[i])) {
      colors[key] = parts[i];
    } else {
      console.warn(`Invalid hex code at index ${i}: ${parts[i]}`);
      colors[key] = '#000000'; // Fallback
    }
  });
  return colors;
}

export function applyThemeColors(colors) {
  if (typeof document === 'undefined') return;
  const root = document.documentElement;
  Object.entries(colors).forEach(([key, value]) => {
    root.style.setProperty(key, value);
  });
}

export function getThemeById(id) {
  return PRESET_THEMES.find((t) => t.id === id) || PRESET_THEMES[0];
}